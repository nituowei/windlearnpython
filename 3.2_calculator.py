# python 计算机程序, 用来练习 if else 语句
operator = input("请输入运算符 (+, -, *, /): ")
num1 = float(input("请输入第一个数字:"))
num2 = float(input("请输入第二个数字:"))
if operator == '+' :
    result = num1 + num2
elif operator == '-' :
    result = num1 - num2
elif operator == '*' :
    result = num1 * num2
elif operator == '/' :
    if num2 == 0:
        result = "除数不能为零"
    else:
        result = num1 / num2
else:
    result = "请输入正确的运算符"
print("结果是: ", result)

# 这个程序是一个简单的计算器，支持加、减、乘、除四种运算
# 1. 首先，程序提示用户输入运算符（+、-、*、/）。
# 2. 然后，程序提示用户输入两个数字。
# 3. 根据用户输入的运算符，程序执行相应的运算。
# 4. 如果运算符是加号（+），程序将两个数字相加。
# 5. 如果运算符是减号（-），程序将第一个数字减去第二个数字。
# 6. 如果运算符是乘号（*），程序将两个数字相乘。
# 7. 如果运算符是除号（/），程序将第一个数字除以第二个数字。
# 8. 如果除数为零，程序将返回错误信息。
# 9. 如果用户输入的运算符不在四种运算符中，程序将返回错误信息。
# 10. 最后，程序将输出运算结果。